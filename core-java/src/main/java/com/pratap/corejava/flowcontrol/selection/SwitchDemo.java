package com.pratap.corejava.flowcontrol.selection;

public class SwitchDemo {

	public static void main(String[] args) {

		int x = 3;
		
		switch (x) {
		
		case 1:
			System.out.println("case-1");
			break;
			
		case 2:
			System.out.println("case-2");
			break;

		default:
			System.out.println("default");
		}
	}

}
